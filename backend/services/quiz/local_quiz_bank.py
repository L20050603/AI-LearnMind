def local_questions(knowledge_point_id: int, count=5):
    base = QUESTION_BANK.get(knowledge_point_id) or QUESTION_BANK[111]
    questions = []
    index = 0
    while len(questions) < count:
        item = base[index % len(base)]
        questions.append({**item, "id": len(questions) + 1, "source": "local-bank"})
        index += 1
    return questions
